# аргумента, и возвращает сумму наибольших двух аргументов.


def my_func():
    a = int(input())
    b = int(input())
    c = int(input())

    if a <= b <= c or c <= b <= a:
        return b + max(a, c)
    elif b <= a <= c or c <= a <= b:
        return a + max(b, c)
    else:
        return c + max(a, b)


