# запрашивать у пользователя, предусмотреть
# обработку ситуации деления на ноль.
def s_calc():
    try:
        num = float(input())
        num1 = float(input())
        cal = num / num1
    except ValueError:
        return 'неправильно введено число'
    except ZeroDivisionError:
        return ('на ноль нельзя делить;(')
    return cal


print(s_calc())

